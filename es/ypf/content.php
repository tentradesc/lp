            separateDialCode: true,
            allowDropdown: true,
            initialCountry: "auto",
            geoIpLookup: function (callback) {
                $.get("https://ipinfo.io", function () { }, "jsonp").always(function (resp) {
                    callback(resp && resp.country ? resp.country : "us");
                });
            }
        });
        // Function to setup the form
        function setupForm(formId, phoneId, countryId, isoId) {
            const form = $(`#${formId}`);
            const telInput = $(`#${phoneId}`);

            // For normal phone input without country flags
            telInput.on("input", function () {
                $(this).removeClass("error-border");  // Remove error border on user input
            });

            form.validate({
                rules: {
                    firstname: "required",
                    lastname: "required",
                    email: {
                        required: true,
                        email: true
                    },
                    phone: "required",
                    terms: "required"
                },
                highlight: function (element) {
                    $(element).addClass("error-border");
                },
                unhighlight: function (element) {
                    $(element).removeClass("error-border");
                },
                errorPlacement: function () {
                    return false;
                },
                submitHandler: function () {
                    form[0].submit(); // Submit the form to thankyou.php
                }
            });
        }

        // Call setup for both forms
